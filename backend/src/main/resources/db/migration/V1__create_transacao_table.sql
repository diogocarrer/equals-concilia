CREATE TABLE transacao (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    codigo_registro VARCHAR(5),
    estabelecimento VARCHAR(50),
    data_inicial DATE,
    data_evento DATE,
    hora_evento TIME,
    tipo_evento VARCHAR(5),
    tipo_transacao VARCHAR(5),
    numero_serie_leitor VARCHAR(20),
    codigo_transacao VARCHAR(50),
    codigo_pedido VARCHAR(50),
    valor_total DECIMAL(18,2),
    valor_parcela_ou_liquido DECIMAL(18,2),
    pagamento VARCHAR(2),
    plano VARCHAR(5),
    parcela VARCHAR(5),
    quantidade_parcelas INT,
    data_prevista_pagamento DATE,
    taxa_parcelamento_comprador DECIMAL(18,2),
    tarifa_boleto_comprador DECIMAL(18,2),
    valor_original DECIMAL(18,2),
    taxa_parcelamento_vendedor DECIMAL(18,2),
    taxa_intermediacao DECIMAL(18,2),
    tarifa_intermediacao DECIMAL(18,2),
    tarifa_boleto_vendedor DECIMAL(18,2),
    repasse_aplicacao DECIMAL(18,2),
    valor_liquido DECIMAL(18,2),
    status_pagamento VARCHAR(5),
    filler VARCHAR(10),
    meio_pagamento VARCHAR(5),
    instituicao_bandeira VARCHAR(50),
    canal_entrada VARCHAR(5),
    leitor VARCHAR(10),
    meio_captura VARCHAR(10),
    numero_logico VARCHAR(50),
    nsu VARCHAR(20),
    filler2 VARCHAR(10),
    cartao_bin VARCHAR(10),
    cartao_holder VARCHAR(10),
    codigo_autorizacao VARCHAR(20),
    codigo_cv VARCHAR(50),
    reservado TEXT
);
